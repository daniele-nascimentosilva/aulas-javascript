/*
A sintaxe do while tem duas partes:

1. **Condição**: até quando o loop de acontecer
2. **Bloco de código**: o que será repetido

while(condicao) {

}
*/

let numero1 = 1
let numero2 = 10

while(numero1 < numero2) {
    console.log(`Incrementando a variável numero1: ${numero1}`);
    numero1++
}
