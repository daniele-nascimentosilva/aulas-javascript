// Variáveis com tipos primitivos em JavaScript
const minhaString = "Olá, mundo!"; // string
const meuNumero = 42; // number
const meuBoolean = true; // boolean
const meuNulo = null; // null
let meuIndefinido; // undefined (declaração sem atribuição)

console.log('string:', minhaString);
console.log('number:', meuNumero);
console.log('boolean:', meuBoolean);
console.log('null:', meuNulo);
console.log('undefined:', meuIndefinido);

